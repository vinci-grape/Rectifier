def main(args):
    n = int(input())
    a = [int(i) for i in input().split()]
    a.sort()
    if a[-1] == 0:
        print(0)
        exit(0)
    while True:
        if a[-1] == 1:
            print(1)
            exit(0)
        a = func(a)
        if len(a) == 1:
            print(a[0])
            exit(0)

def func(a):
    a.sort()
    min = 0
    for i in range(len(a)):
        if a[i] == 0:
            a.pop(i)
            i -= 1
        else:
            if min!= 0:
                a[i] %= min
            else:
                min = a[i]
    a.sort()
    return a

