def main(args):
    sc = Scanner(sys.stdin)
    N = sc.nextInt()
    size = 2**N
    S = [sc.nextInt() for _ in range(size)]
    S.sort()
    active = []
    active.append(S[size-1])
    spawned = [False]*size
    spawned[size-1] = True
    for i in range(N):
        active.sort(reverse=True)
        activated = []
        next = size-1
        while next >= 0 and (S[next] >= S[next] or spawned[next]):
            next -= 1
        if next < 0:
            print("No")
            return
        spawned[next] = True
        activated.append(S[next])
        active.extend(activated)
    print("Yes")

