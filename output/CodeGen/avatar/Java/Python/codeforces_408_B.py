def isValid(s1, s2):
    s1 = list(s1)
    s2 = list(s2)
    n1 = len(s1)
    n2 = len(s2)
    if n1!= n2:
        return False
    m1 = {}
    m2 = {}
    for c in s1:
        if c in m1:
            m1[c] += 1
        else:
            m1[c] = 1
    for c in s2:
        if c in m2:
            m2[c] += 1
        else:
            m2[c] = 1
    ans = 0
    hs = set()
    for c in s2:
        if c not in hs:
            hs.add(c)
            x1 = m1[c]
            x2 = m2[c]
            x1 = min(x1, x2)
            ans += x1
    return ans == len(s2)

